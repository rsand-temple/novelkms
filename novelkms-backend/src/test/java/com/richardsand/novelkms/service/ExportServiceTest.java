    @Test
    void filenameSanitizesIllegalCharactersFromBookTitle() throws Exception {
        Book messyBook = bookDao.create(project.getId(), "Book: The \"Sequel\"?", null, null, null);
        Chapter chapter = chapterDao.create(messyBook.getId(), null, "Ch", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(), "<p>Content.</p>", 1);

        ExportService.ExportMeta meta = service.exportBook(messyBook.getId());

        assertFalse(meta.filename().contains(":"));
        assertFalse(meta.filename().contains("\""));
        assertFalse(meta.filename().contains("?"));
        assertTrue(meta.filename().endsWith(".docx"));
    }

    // -------------------------------------------------------------------------
    // Helpers
    // -------------------------------------------------------------------------

    private List<String> paragraphTexts(byte[] docxBytes) throws Exception {
        try (XWPFDocument doc = new XWPFDocument(new ByteArrayInputStream(docxBytes))) {
            return doc.getParagraphs().stream()
                    .map(XWPFParagraph::getText)
                    .toList();
        }
    }

    private int indexOfContaining(List<String> paragraphs, String needle) {
        for (int i = 0; i < paragraphs.size(); i++) {
            if (paragraphs.get(i).contains(needle)) {
                return i;
            }
        }
        return -1;
    }

    /** A minimal valid 1x1 PNG, generated via ImageIO so it round-trips through ImageIO.read(). */
    private byte[] tinyPng() throws Exception {
        BufferedImage image = new BufferedImage(1, 1, BufferedImage.TYPE_INT_RGB);
        image.setRGB(0, 0, 0xFFFFFF);
        try (ByteArrayOutputStream out = new ByteArrayOutputStream()) {
            ImageIO.write(image, "png", out);
            return out.toByteArray();
        }
    }
