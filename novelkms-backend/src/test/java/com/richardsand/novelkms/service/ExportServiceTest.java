    @Test
    void exportBookResolvesCoverTemplateTokens() throws Exception {
        Project updatedProject = Project.builder()
                .id(project.getId())
                .title(project.getTitle())
                .authorFirstName("Jane")
                .authorLastName("Doe")
                .copyright("\u00a9 2026 Jane Doe")
                .build();
        projectDao.update(updatedProject);

        templateDao.upsertBookOverride(book.getId(), TemplateDao.TYPE_COVER, """
                <p data-style="cover_title" style="text-align:center"><span data-token="TITLE"></span></p>
                <p data-style="cover_subtitle" style="text-align:center">By <span data-token="AUTHOR_FULL_NAME"></span></p>
                """);

        Chapter chapter = chapterDao.create(book.getId(), null, "Ch", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(),
                "<p>One two three four five.</p>", 5);

        ExportService.ExportMeta meta = service.exportBook(book.getId());

        List<String> paragraphs = paragraphTexts(meta.bytes());
        assertTrue(paragraphs.stream().anyMatch(p -> p.equals(book.getTitle())));
        assertTrue(paragraphs.stream().anyMatch(p -> p.equals("By Jane Doe")));
    }

