    @Test
    void exportAppliesConfiguredCustomPageSizeToMainContentSection() throws Exception {
        pageLayoutDao.upsertBook(book.getId(), PageLayout.builder()
                .pageLayoutEnabled(true)
                .pageSizePreset("CUSTOM")
                .pageWidthIn(6.0)
                .pageHeightIn(9.0)
                .pageMarginTopIn(0.75)
                .pageMarginBottomIn(0.75)
                .pageMarginInnerIn(0.5)
                .pageMarginOuterIn(0.5)
                .build());

        Chapter chapter = chapterDao.create(book.getId(), null, "Ch", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(), "<p>Content.</p>", 1);

        ExportService.ExportMeta meta = service.exportBook(book.getId());

        try (XWPFDocument doc = new XWPFDocument(new ByteArrayInputStream(meta.bytes()))) {
            CTSectPr mainSectPr = doc.getDocument().getBody().getSectPr();
            assertEquals(BigInteger.valueOf(Math.round(6.0 * 1440)), mainSectPr.getPgSz().getW());
            assertEquals(BigInteger.valueOf(Math.round(9.0 * 1440)), mainSectPr.getPgSz().getH());
            assertEquals(BigInteger.valueOf(Math.round(0.75 * 1440)), mainSectPr.getPgMar().getTop());
            assertEquals(BigInteger.valueOf(Math.round(0.5 * 1440)), mainSectPr.getPgMar().getLeft());
        }
    }

    @Test
    void exportAppliesConfiguredCustomPageSizeToCoverSectionToo() throws Exception {
        pageLayoutDao.upsertBook(book.getId(), PageLayout.builder()
                .pageLayoutEnabled(true)
                .pageSizePreset("CUSTOM")
                .pageWidthIn(6.0)
                .pageHeightIn(9.0)
                .pageMarginTopIn(0.75)
                .pageMarginBottomIn(0.75)
                .pageMarginInnerIn(0.5)
                .pageMarginOuterIn(0.5)
                .build());

        bookDao.setCoverImage(book.getId(), tinyPng(), "image/png");
        Chapter chapter = chapterDao.create(book.getId(), null, "Ch", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(), "<p>Content.</p>", 1);

        ExportService.ExportMeta meta = service.exportBook(book.getId());

        try (XWPFDocument doc = new XWPFDocument(new ByteArrayInputStream(meta.bytes()))) {
            // The cover section is the embedded sectPr on the first paragraph
            // that carries one (the section-break paragraph inserted right
            // after the cover image/title page). It must match the same
            // custom size as the document-level (main content) section.
            CTSectPr coverSectPr = doc.getParagraphs().stream()
                    .map(p -> p.getCTP().getPPr())
                    .filter(pPr -> pPr != null && pPr.isSetSectPr())
                    .map(pPr -> pPr.getSectPr())
                    .findFirst()
                    .orElseThrow(() -> new AssertionError("Expected an embedded cover-section sectPr"));

            assertEquals(BigInteger.valueOf(Math.round(6.0 * 1440)), coverSectPr.getPgSz().getW());
            assertEquals(BigInteger.valueOf(Math.round(9.0 * 1440)), coverSectPr.getPgSz().getH());
            assertEquals(BigInteger.valueOf(Math.round(0.75 * 1440)), coverSectPr.getPgMar().getTop());
            assertEquals(BigInteger.valueOf(Math.round(0.5 * 1440)), coverSectPr.getPgMar().getLeft());
        }
    }

