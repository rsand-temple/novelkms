    @Test
    void exportBookOrdersPartChaptersBeforeDirectChapters() throws Exception {
        Part part = partDao.create(book.getId(), "Part One", null, null);
        Chapter inPart = chapterDao.create(book.getId(), part.getId(), "In The Part", null, null);
        sceneDao.saveContent(sceneDao.create(inPart.getId(), "s", null).getId(), "<p>Inside the part.</p>", 3);
        Chapter direct = chapterDao.create(book.getId(), null, "Direct Chapter", null, null);
        sceneDao.saveContent(sceneDao.create(direct.getId(), "s", null).getId(), "<p>Outside any part.</p>", 3);

        ExportService.ExportMeta meta = service.exportBook(book.getId());

        List<String> paragraphs = paragraphTexts(meta.bytes());
        int partIdx = paragraphs.indexOf("Part One");
        int inPartIdx = paragraphs.indexOf("In The Part");
        int directIdx = paragraphs.indexOf("Direct Chapter");
        assertTrue(partIdx < inPartIdx);
        assertTrue(inPartIdx < directIdx);
    }

