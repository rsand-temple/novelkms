    @Test
    void filenameSanitizesIllegalCharactersFromBookTitle() throws Exception {
        Book    messyBook = bookDao.create(project.getId(), "Book: The \"Sequel\"?", null, null, null);
        Chapter chapter   = chapterDao.create(messyBook.getId(), null, "Ch", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(), "<p>Content.</p>", 1);

        PdfExportService.ExportMeta meta = service.exportBook(messyBook.getId());

        assertFalse(meta.filename().contains(":"));
        assertFalse(meta.filename().contains("\""));
        assertFalse(meta.filename().contains("?"));
        assertTrue(meta.filename().endsWith(".pdf"));
    }

    // -------------------------------------------------------------------------
    // Helpers
    // -------------------------------------------------------------------------

    private String fullText(byte[] pdfBytes) throws Exception {
        try (PDDocument doc = Loader.loadPDF(pdfBytes)) {
            return new PDFTextStripper().getText(doc);
        }
    }

    /** Extracts the text of a single 1-based page index. */
    private String pageText(PDDocument doc, int pageNumberOneBased) throws Exception {
        PDFTextStripper stripper = new PDFTextStripper();
        stripper.setStartPage(pageNumberOneBased);
        stripper.setEndPage(pageNumberOneBased);
        return stripper.getText(doc);
    }

    /** A minimal valid 1x1 PNG, generated via ImageIO so it round-trips through decoding. */
    private byte[] tinyPng() throws Exception {
        BufferedImage image = new BufferedImage(1, 1, BufferedImage.TYPE_INT_RGB);
        image.setRGB(0, 0, 0xFFFFFF);
        try (ByteArrayOutputStream out = new ByteArrayOutputStream()) {
            ImageIO.write(image, "png", out);
            return out.toByteArray();
        }
    }
