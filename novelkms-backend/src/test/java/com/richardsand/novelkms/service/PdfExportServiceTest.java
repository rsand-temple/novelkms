    @Test
    void exportAppliesConfiguredCustomPageSizeToCoverPagesToo() throws Exception {
        pageLayoutDao.upsertBook(book.getId(), PageLayout.builder()
                .pageLayoutEnabled(true)
                .pageSizePreset("CUSTOM")
                .pageWidthIn(6.0)
                .pageHeightIn(9.0)
                .pageMarginTopIn(0.75)
                .pageMarginBottomIn(0.75)
                .pageMarginInnerIn(0.5)
                .pageMarginOuterIn(0.5)
                .build());

        bookDao.setCoverImage(book.getId(), tinyPng(), "image/png");
        Chapter chapter = chapterDao.create(book.getId(), null, "Ch", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(), "<p>Content.</p>", 1);

        PdfExportService.ExportMeta meta = service.exportBook(book.getId());

        try (PDDocument doc = Loader.loadPDF(meta.bytes())) {
            double expectedWidthPt  = 6.0 * 72.0;
            double expectedHeightPt = 9.0 * 72.0;

            PDRectangle coverPage = doc.getPage(0).getMediaBox();
            assertEquals(expectedWidthPt, coverPage.getWidth(), 1.0);
            assertEquals(expectedHeightPt, coverPage.getHeight(), 1.0);

            PDRectangle lastPage = doc.getPage(doc.getNumberOfPages() - 1).getMediaBox();
            assertEquals(expectedWidthPt, lastPage.getWidth(), 1.0);
            assertEquals(expectedHeightPt, lastPage.getHeight(), 1.0);
        }
    }

    @Test
    void exportUsesLetterSizeWhenPageLayoutDisabled() throws Exception {
        Chapter chapter = chapterDao.create(book.getId(), null, "Ch", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(), "<p>Content.</p>", 1);

        PdfExportService.ExportMeta meta = service.exportBook(book.getId());

        try (PDDocument doc = Loader.loadPDF(meta.bytes())) {
            PDRectangle page = doc.getPage(0).getMediaBox();
            assertEquals(8.5 * 72.0, page.getWidth(), 1.0);
            assertEquals(11.0 * 72.0, page.getHeight(), 1.0);
        }
    }

    // -------------------------------------------------------------------------
    // Running header
    // -------------------------------------------------------------------------

    @Test
    void runningHeaderAppearsOnBodyPagesButNotCoverPages() throws Exception {
        Project updatedProject = Project.builder()
                .id(project.getId())
                .title(project.getTitle())
                .authorLastName("Sand")
                .build();
        projectDao.update(updatedProject);

        bookDao.setCoverImage(book.getId(), tinyPng(), "image/png");
        Chapter chapter = chapterDao.create(book.getId(), null, "Ch One", null, null);
        sceneDao.saveContent(sceneDao.create(chapter.getId(), "s", null).getId(), "<p>Body content.</p>", 2);

        PdfExportService.ExportMeta meta = service.exportBook(book.getId());

        // The running header format is "LastName / ShortTitle / {page}". Check
        // for that distinctive slash-separated fingerprint rather than a bare
        // "Sand" substring — the default SYSTEM cover template's own "By
        // {AUTHOR_FULL_NAME}" line legitimately renders "Sand" as body content
        // on the title page, which would otherwise collide with this check.
        String headerFingerprint = "Sand / " + book.getShortTitle() + " /";

        try (PDDocument doc = Loader.loadPDF(meta.bytes())) {
            // Page 1 = cover image, page 2 = title page. Neither should carry
            // the running header (mirrors DOCX's section-break exclusion).
            assertFalse(pageText(doc, 1).contains(headerFingerprint));
            assertFalse(pageText(doc, 2).contains(headerFingerprint));

            // The last page is manuscript body content and must carry it.
            String lastPageText = pageText(doc, doc.getNumberOfPages());
            assertTrue(lastPageText.contains(headerFingerprint));
        }
    }

